parse_args() {
    if [[ $# -eq 0 ]]; then
        DO_BUILD=true
        return
    fi

    while [[ $# -gt 0 ]]; do
        case $1 in
            -b|--build)
                DO_BUILD=true
                shift
                ;;
            -c|--clean)
                DO_CLEAN=true
                shift
                ;;
            -i|--install)
                DO_INSTALL=true
                shift
                ;;
            -u|--ut)
                DO_UNIT_TEST=true
                shift
                ;;
            -s|--smoke)
                DO_SMOKE_TEST=true
                shift
                ;;
            -a|--all)
                DO_BUILD=true
                DO_UNIT_TEST=true
                DO_SMOKE_TEST=true
                shift
                ;;
            -f|--filelist)
                PR_FILELIST="$2"
                shift 2
                shift
                ;;
            # Report option
            --generate-report)
                DO_GENERATE_REPORT=true
                shift
                ;;
            -h|--help)
                show_help
                ;;
            *)
                log_error "Unknown param: $1"
                exit 1
                ;;
        esac
    done
}


