# Unit test
do_unit_test() {
    log_info "Start unit tests..."

    cd "$PROJECT_ROOT"

    python3 -m pip install pytest pytest-html pytest-cov pytest-mock

    log_info "listing dependencies..."
    python3 -m pip list

    local overall_exit_code=0

    # Incremental test mode: only run local tests filtered by PR_FILELIST
    if [[ -n "$PR_FILELIST" && -f "$PR_FILELIST" ]]; then
        log_info "Incremental test mode enabled (PR_FILELIST: $PR_FILELIST)"
        run_npu_tests
        local npu_exit=$?
        if [[ $npu_exit -ne 0 && $npu_exit -ne 5 ]]; then
            log_error "Incremental tests failed!"
            exit 1
        fi
        log_info "Incremental tests completed!"
        return 0
    fi

    # Full test mode: run the configured UT phases.
    if [[ "$RUN_TORCHTITAN_UT" == "true" ]]; then
        run_torchtitan_tests
        local titan_exit=$?
        if [[ $titan_exit -ne 0 && $titan_exit -ne 5 ]]; then
            overall_exit_code=1
        fi
    else
        log_info "Skipping torchtitan upstream unit tests (RUN_TORCHTITAN_UT=${RUN_TORCHTITAN_UT})"
    fi

    run_npu_tests
    local npu_exit=$?
    if [[ $npu_exit -ne 0 && $npu_exit -ne 5 ]]; then
        overall_exit_code=1
    fi

    if [[ $overall_exit_code -eq 0 ]]; then
        write_report_index
        log_info "All unit tests passed!"
    else
        write_report_index
        log_error "Some unit tests failed!"
        exit 1
    fi
}

# only add increment tests
get_affected_tests() {
    local filelist="$1"
    local test_files=""

    while IFS= read -r changed_file || [[ -n "$changed_file" ]]; do
        [[ -z "$changed_file" || "$changed_file" == \#* ]] && continue

        if [[ "$changed_file" == torchtitan_npu/*.py ]]; then
            local module_name=$(basename "changed_file" .py)
            local test_file="tests/test_${module_name}.py"
            if [[ -f "$test_file" ]]; then
                test_files="$test_files $test_file"
            fi
        fi

        if [[ "$changed_file" == tests/*.py ]]; then
            if [[ -f "changed_file" ]]; then
                test_files="$test_files $test_file"
            fi
        fi
    done < "$filelist"

    echo "$test_files" | tr ' ' '\n' | sort -u | tr '\n' ' '
}

# Run upstream integration tests (torchtitan source with NPU patches)
# Mapping to upstream integration coverage:
# - S-044: model init / basic startup path
# - S-045: dataloader path in upstream smoke suite
# - S-046: forward path
# - S-047: backward path
# - S-048: checkpoint save/load path
# The concrete upstream cases are selected by torchtitan's integration suite
# under tests/integration_tests/.
run_upstream_smoke() {
    log_info "Running torchtitan upstream integration tests..."
    mkdir -p "$REPORT_DIR"

    # Ensure torchtitan_npu is installed
    if ! python3 -c "import torchtitan_npu" 2>/dev/null; then
        python3 -m pip install -e .
    fi

    # Check if torchtitan source exists
    if [[ ! -d "$TITAN_DIR" ]]; then
        log_info "Cloning torchtitan source..."
        mkdir -p third_party
        git clone --branch $TITAN_VERSION --depth 1 \
            https://gitcode.com/GitHub_Trending/to/torchtitan.git $TITAN_DIR
    fi

    local smoke_log="${REPORT_DIR}/upstream_smoke.log"
    local upstream_output_dir="${REPORT_DIR}/upstream_integration_output"
    local start_time=$(date +%s)
    local titan_test_dir="${TITAN_DIR}/tests/integration_tests"
    local conftest_file="${titan_test_dir}/conftest.py"
    local saved_pythonpath="$PYTHONPATH"

    if [[ ! -d "$titan_test_dir" ]]; then
        log_error "Torchtitan integration test directory not found: $titan_test_dir"
        return 1
    fi

    cd "$TITAN_DIR"

    rm -rf "$upstream_output_dir"
    mkdir -p "$upstream_output_dir"

    # Ensure torchtitan_npu patches are applied before upstream integration cases import torchtitan modules.
    cat > "$conftest_file" << 'EOF'
# Auto-generated conftest for torchtitan-npu upstream integration testing
def pytest_configure(config):
    import torchtitan_npu  # noqa: F401
EOF

    export PYTHONPATH="${TITAN_DIR}:${PROJECT_ROOT}:${PYTHONPATH}"

    # Run upstream integration tests with NPU patches applied.
    local cmd=(
        python3 -m tests.integration_tests.run_tests
        "$upstream_output_dir"
        --test_suite "$UPSTREAM_TEST_SUITE"
        --test_name "$UPSTREAM_TEST_NAME"
        --ngpu "$NGPU"
    )

    set +e
    "${cmd[@]}" 2>&1 | tee "$smoke_log"
    local exit_code=${PIPESTATUS[0]}
    set -e

    cd "$PROJECT_ROOT"
    rm -f "$conftest_file"
    export PYTHONPATH="$saved_pythonpath"

    local end_time=$(date +%s)
    local duration=$((end_time - start_time))
    log_info "Upstream integration test duration: ${duration}s"

    if [[ $exit_code -eq 0 ]]; then
        log_info "Upstream integration tests passed!"
    else
        log_error "Upstream smoke failed; stopping smoke pipeline."
        log_error "Upstream integration tests failed (exit_code=$exit_code)"
        return $exit_code
    fi
}

# Run extended local smoke suites
run_extended_smoke() {
    log_info "Running extended smoke suites..."
    mkdir -p "$REPORT_DIR"

    local smoke_targets=(
        "tests/smoke_tests/features/"
        "tests/smoke_tests/model_parallel/"
    )
    local available_targets=()
    local target
    for target in "${smoke_targets[@]}"; do
        if [[ -d "$target" ]]; then
            available_targets+=("$target")
        fi
    done

    if [[ ${#available_targets[@]} -eq 0 ]]; then
        log_info "No extended smoke directories found, skipping..."
        return 0
    fi

    local smoke_log="${REPORT_DIR}/extended_smoke.log"
    local pytest_args=(-v --tb=short -m "not nightly")
    pytest_args+=($(build_pytest_report_args "extended_smoke_report"))

    set +e
    python3 -m pytest "${available_targets[@]}" "${pytest_args[@]}" 2>&1 | tee "$smoke_log"
    local exit_code=${PIPESTATUS[0]}
    set -e

    if [[ $exit_code -eq 0 ]]; then
        log_info "Extended smoke suites passed!"
    else
        log_error "Extended smoke failed; stopping smoke pipeline."
        log_error "Extended smoke suites failed (exit_code=$exit_code)"
        return $exit_code
    fi
}

