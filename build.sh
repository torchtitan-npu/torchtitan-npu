    set +e 
    timeout $TIMEOUT_SECONDS bash -c "
        export NGPU=$NGPU
        export CONFIG_FILE="${SMOKE_CONFIG}"
        ./run_train.sh
    " 2>&1 | tee "$smoke_log"
    local exit_code=${PIPESTATUS[0]}
    set -e

    local end_time=$(date +%s)
    local duration=$((end_time - start_time))
    log_info "duration smoke time: ${duration}s"
    if ! analyse_smoke_result "$smoke_log" "$exit_code"; then
        log_error "Core smoke failed; stopping smoke pipeline."
        return 1
    fi
}

# Main smoke test entry
do_smoke_test() {
    local run_core=true
    local run_extended=true
    local run_upstream=false
    local smoke_stages=()

    python3 -m pip install pytest pytest-html pytest-cov pytest-mock

    if [[ "$ONLY_CORE_SMOKE" == "true" || "$ONLY_EXTENDED_SMOKE" == "true" || "$ONLY_UPSTREAM_SMOKE" == "true" ]]; then
        run_core=false
        run_extended=false
        run_upstream=false
        [[ "$ONLY_CORE_SMOKE" == "true" ]] && run_core=true
        [[ "$ONLY_EXTENDED_SMOKE" == "true" ]] && run_extended=true
        [[ "$ONLY_UPSTREAM_SMOKE" == "true" ]] && run_upstream=true
    fi

    [[ "$run_core" == "true" ]] && smoke_stages+=("run_core_smoke")
    [[ "$run_extended" == "true" ]] && smoke_stages+=("run_extended_smoke")
    [[ "$run_upstream" == "true" ]] && smoke_stages+=("run_upstream_smoke")

    local stage_runner
    for stage_runner in "${smoke_stages[@]}"; do
        if ! "$stage_runner"; then
            write_report_index
            log_error "smoke test failed"
            exit 1
        fi
    done

    write_report_index
    log_info "smoke test pass"
}

analyse_smoke_result() {
    local log_file="$1"
    local exit_code="$2"

    log_info "Analysing result of smoke test"
    local has_error=false

    if [[ "$exit_code" -eq 124 ]]; then
        log_error "Timeout!"
        has_error=true
    fi

    if grep -qiE "error|exception|traceback" "$log_file" 2>/dev/null; then
        if grep -qiE "NPU error|RuntimeError|OOM|OutOfMemory" "$log_file"; then
            log_error "error detected during running"
            grep -iE "NPU error|RuntimeError|OOM|OutOfMemory" "$log_file" | head -10
            has_error=true
        fi
    fi

    if echo "$clean_log" | grep -qiE "loss[^:]*:[^0-9]*(nan|inf)" \
    || echo "$clean_log" | grep -qiE "(nan|inf).*loss"; then
        log_error "loss error (NAN/Inf)"
        has_error=true
    fi

    local complete_steps=$(grep -oP "step[:\s]*\K\d+" "$log_file" 2>/dev/null | tail -1 || echo "0")
    if [[ "$completed_steps" -ge "$SMOKE_STEPS" ]]; then
        log_info "Completed $complete_steps steps"
    elif [[ "$completed_steps" -gt 0 ]]; then
        log_error "Only finished $complete_steps/$SMOKE_STEPS steps"
    else
        log_error "No output steps detected"
    fi

    if [[ "$has_error" == "true" ]] || [[ "$exit_code" -ne 0 ]]; then
        return 1
    fi

    return 0
}

