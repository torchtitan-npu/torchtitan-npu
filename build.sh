build_pytest_report_args() {
    local report_name="$1"
    local args="--junitxml=${REPORT_DIR}/${report_name}.xml"

    if [[ "$DO_GENERATE_REPORT" == "true" ]]; then
        args="$args --html=${REPORT_DIR}/${report_name}.html --self-contained-html"
    fi

    echo "$args"
}

write_report_index() {
    local index_file="${REPORT_DIR}/README.md"

    mkdir -p "$REPORT_DIR"
    cat > "$index_file" << EOF
# Test Reports

Generated by \`build.sh\`.

## Available Artifacts

EOF

    local artifact
    for artifact in "$REPORT_DIR"/*.xml "$REPORT_DIR"/*.html "$REPORT_DIR"/*.log; do
        if [[ -f "$artifact" ]]; then
            printf -- "- %s\n" "$(basename "$artifact")" >> "$index_file"
        fi
    done

    log_info "Report index updated: ${index_file}"
}

# Python 3.10 paths
PYTHON310_INSTALL_DIR="/usr/local/python3.10.18"
PYTHON310_LINK_DIR="/usr/local/python/python310"

detect_pkg_manager() {
    if command -v apt-get >/dev/null 2>&1; then
        echo "apt"
    elif command -v yum >/dev/null 2>&1; then
        echo "yum"
    elif command -v dnf >/dev/null 2>&1; then
        echo "dnf"
    else
        return 1
    fi
}

install_python_build_deps() {
    local pkg_manager
    pkg_manager=$(detect_pkg_manager) || {
        log_error "Unsupported package manager. Please install Python build dependencies manually."
        return 1
    }

    case "$pkg_manager" in
        apt)
            apt-get update
            apt-get install -y \
                build-essential wget libbz2-dev liblzma-dev libsqlite3-dev \
                libffi-dev libreadline-dev libncurses5-dev zlib1g-dev libssl-dev
            ;;
        yum)
            yum install -y \
                gcc gcc-c++ make wget bzip2-devel xz-devel sqlite-devel \
                libffi-devel readline-devel ncurses-devel zlib-devel openssl-devel
            ;;
        dnf)
            dnf install -y \
                gcc gcc-c++ make wget bzip2-devel xz-devel sqlite-devel \
                libffi-devel readline-devel ncurses-devel zlib-devel openssl-devel
            ;;
    esac
}

# Setup Python 3.10 environment
setup_python310() {
    local python_bin_dir="${PYTHON310_LINK_DIR}/bin"

    # Check if python3.10 exists and has required modules
    local need_install=false
    if [[ ! -x "${python_bin_dir}/python3" ]]; then
        log_info "Python 3.10 not found at ${python_bin_dir}"
        need_install=true
    elif ! "${python_bin_dir}/python3" -c "import sqlite3, bz2, lzma" 2>/dev/null; then
        log_info "Python 3.10 missing required modules (sqlite3, bz2, lzma)"
        need_install=true
    fi

    # Install Python 3.10 if needed
    if [[ "$need_install" == "true" ]]; then
        log_info "Installing Python 3.10..."

        echo "  Installing build dependencies "
        install_python_build_deps

        echo "  Downloading Python 3.10 source "
        cd /tmp
        wget --no-check-certificate https://www.python.org/ftp/python/3.10.18/Python-3.10.18.tgz
        tar -xzf Python-3.10.18.tgz
        cd Python-3.10.18

        echo "  Compiling and installing "
        ./configure --prefix="$PYTHON310_INSTALL_DIR"
        make -j$(nproc)
        make install

        echo "  Creating symbolic links "
        rm -rf "$PYTHON310_LINK_DIR"
        ln -sf "$PYTHON310_INSTALL_DIR" "$PYTHON310_LINK_DIR"

        echo "  Verifying "
        "${python_bin_dir}/python3" -c "import sqlite3, bz2, lzma; print('Modules complete: ok')"
        "${python_bin_dir}/python3" --version

        cd "$PROJECT_ROOT"
    fi

    # Create symlinks for python/pip (if python3/pip3 exist but python/pip don't)
    ln -sf "${python_bin_dir}/python3" "${python_bin_dir}/python" 2>/dev/null || true
    ln -sf "${python_bin_dir}/pip3" "${python_bin_dir}/pip" 2>/dev/null || true

    # Add Python bin dir to PATH with highest priority
    export PATH="${python_bin_dir}:$PATH"

    log_info "Python 3.10 ready: $(which python)"
}

